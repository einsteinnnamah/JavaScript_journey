


//Comparism Operators
// *******************//

// Comparison Operators are operators used to compare two values e.g if two numbers are equal, if one number are greater than eachother
// Note: all comparison operators return a boolean value i.e true or false
//*******************//

//Greater than
// console.log(2 > 1) //true

// //less than
// console.log(1 < 2) //true

// //Greater than or equal to
// console.log(2>=2) //true

// //Less than or equal to
// console.log(2<=1) //false

// //Equality
// console.log(2==1) //false

// //Inequality
// console.log(2!=1) //true

// //Strict equality
// console.log(2===1) //false


// //Assignment Operators
// let size = 10;
// the assignment operator is used to assign a value to a variable
//Do not confuse assignment operator with the equality operator

//increment operator
// size ++ / increases the value of size by one

// Decrement operator
// size -- / decreases the value of size by one

//Addition assignment operator
// size += 10 / adds 10 to size

//Subtraction assignment operator
// size -= 10 / subtracts 10 from size

